def test_markdown_special_chars_escaped():
    bullets = [_bullet(content="use `foo` *and* [bar] and _baz_")]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    # Backslash-escaped forms.
    assert r"\`foo\`" in out
    assert r"\*and\*" in out
    assert r"\[bar\]" in out
    assert r"\_baz\_" in out


def test_backslash_itself_escaped():
    bullets = [_bullet(content=r"path\to\file")]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert r"path\\to\\file" in out


# ---------------------------------------------------------------------------
# Unicode truncation
# ---------------------------------------------------------------------------

def test_unicode_truncation_code_points_not_bytes():
    # 200 Chinese chars = 600 UTF-8 bytes; truncation should use code points.
    content = "中" * 200
    bullets = [_bullet(content=content)]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    # Should contain exactly 120 "中" characters in the bullet line.
    line = [l for l in out.splitlines() if "中" in l][0]
    assert line.count("中") == 120


def test_unicode_truncation_short_content_unchanged():
    bullets = [_bullet(content="short")]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "short" in out


def test_unicode_truncation_emoji_counted_by_code_points():
    # 200 emoji each 1 code point (BMP-style heart); truncate to 120.
    content = "❤" * 200
    bullets = [_bullet(content=content)]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    line = [l for l in out.splitlines() if "❤" in l][0]
    assert line.count("❤") == 120


