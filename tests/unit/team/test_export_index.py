def test_deterministic_same_input_same_output():
    bullets = [
        _bullet(tag="python", content="p1", recall_count=3),
        _bullet(tag="rust", content="r1", recall_count=5),
        _bullet(tag="python", content="p2", recall_count=3,
                created_at="2026-02-01T00:00:00Z"),
    ]
    a = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    # Shuffle input order — result must be identical.
    b = generate_index_md(list(reversed(bullets)), "team:x", now=FIXED_NOW)
    assert a == b


def test_trailing_newline_present():
    out = generate_index_md([], "team:x", now=FIXED_NOW)
    assert out.endswith("\n")
    assert not out.endswith("\n\n\n")


