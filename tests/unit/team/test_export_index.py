def test_groups_sorted_alphabetically():
    bullets = [
        _bullet(tag="rust", content="r1"),
        _bullet(tag="ansible", content="a1"),
        _bullet(tag="python", content="p1"),
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    i_ansible = out.index("## ansible")
    i_python = out.index("## python")
    i_rust = out.index("## rust")
    assert i_ansible < i_python < i_rust


def test_intra_group_sorted_by_recall_desc_then_created_asc():
    bullets = [
        _bullet(tag="python", content="low-old", recall_count=1,
                created_at="2025-01-01T00:00:00Z"),
        _bullet(tag="python", content="high-new", recall_count=10,
                created_at="2026-01-01T00:00:00Z"),
        _bullet(tag="python", content="high-old", recall_count=10,
                created_at="2024-01-01T00:00:00Z"),
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    # Expected order: high-old, high-new, low-old
    i_ho = out.index("high-old")
    i_hn = out.index("high-new")
    i_lo = out.index("low-old")
    assert i_ho < i_hn < i_lo


def test_group_header_includes_count():
    bullets = [
        _bullet(tag="python", content="p1"),
        _bullet(tag="python", content="p2"),
        _bullet(tag="rust", content="r1"),
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "## python (2)" in out
    assert "## rust (1)" in out


def test_tag_precedence_falls_back_to_tags_then_section():
    bullets = [
        {"content": "a", "tags": ["python", "other"], "recall_count": 0},
        {"content": "b", "section": "rust", "recall_count": 0},
        {"content": "c", "recall_count": 0},  # falls back to "general"
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "## python (1)" in out
    assert "## rust (1)" in out
    assert "## general (1)" in out


