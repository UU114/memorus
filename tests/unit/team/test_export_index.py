def test_missing_recall_count_defaults_to_zero():
    bullets = [{"content": "x", "tag": "python", "decay_weight": 0.5}]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "`[#0]`" in out


def test_missing_decay_weight_defaults_to_one():
    bullets = [{"content": "x", "tag": "python", "recall_count": 0}]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "weight: 1.00" in out


def test_created_at_accepts_datetime_object():
    dt = datetime(2026, 5, 1, tzinfo=timezone.utc)
    bullets = [
        _bullet(tag="python", content="old", recall_count=5, created_at=dt),
        _bullet(tag="python", content="new", recall_count=5,
                created_at="2026-06-01T00:00:00Z"),
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert out.index("old") < out.index("new")


def test_chinese_tag_sorts_and_renders():
    bullets = [
        _bullet(tag="python", content="p"),
        _bullet(tag="中文", content="c"),
    ]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
