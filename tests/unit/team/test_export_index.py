def test_bullet_format_basic():
    bullets = [_bullet(content="foo", recall_count=7, decay_weight=0.94)]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "- `[#7]` foo *(weight: 0.94)*" in out


def test_bullet_format_with_sources():
    bullets = [_bullet(content="foo", recall_count=7, decay_weight=0.94,
                       sources_count=3)]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "- `[#7]` foo *(weight: 0.94, sources: 3)*" in out


def test_bullet_format_with_source_refs_list():
    bullets = [_bullet(content="foo", recall_count=7, decay_weight=0.94,
                       source_refs=["a", "b", "c", "d", "e"])]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "sources: 5" in out


def test_bullet_format_without_sources_omits_suffix():
    # No sources-related field at all — suffix omitted cleanly.
    bullets = [_bullet(content="foo", recall_count=7, decay_weight=0.94)]
    out = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert "sources" not in out.split("foo", 1)[1].split("\n", 1)[0]


