def test_write_index_md_creates_directory(tmp_path: Path):
    out_dir = tmp_path / "nested" / ".ace"
    assert not out_dir.exists()
    target = write_index_md([], "team:x", out_dir, now=FIXED_NOW)
    assert target == out_dir / "index.md"
    assert target.exists()
    assert target.read_text(encoding="utf-8").startswith("# Team Playbook Index")


def test_write_index_md_roundtrip(tmp_path: Path):
    bullets = [_bullet(content="alpha"), _bullet(tag="rust", content="beta")]
    target = write_index_md(bullets, "team:x", tmp_path, now=FIXED_NOW)
    text = target.read_text(encoding="utf-8")
    expected = generate_index_md(bullets, "team:x", now=FIXED_NOW)
    assert text == expected


