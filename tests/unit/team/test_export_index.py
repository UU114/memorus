"""Tests for team playbook index.md generator (STORY-R093)."""

from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path

import pytest

from memorus.team.export import generate_index_md, write_index_md


# Fixed timestamp for deterministic tests.
FIXED_NOW = datetime(2026, 4, 23, 10, 15, 0, tzinfo=timezone.utc)


def _bullet(**kwargs):
    base = {
        "content": "hello",
        "tag": "python",
        "recall_count": 1,
        "decay_weight": 0.5,
        "created_at": "2026-01-01T00:00:00Z",
    }
    base.update(kwargs)
    return base


# ---------------------------------------------------------------------------
# Header & empty playbook
# ---------------------------------------------------------------------------

def test_empty_playbook_produces_legal_index():
    out = generate_index_md([], "team:backend", now=FIXED_NOW)
    assert out.startswith("# Team Playbook Index\n")
    assert "**Total bullets:** 0" in out
    assert "**Scope:** team:backend" in out
    assert "**Last updated:** 2026-04-23T10:15:00Z" in out
    assert out.endswith("\n")
    # No tag groups should appear.
    assert "## " not in out
    # Footer present.
    assert "Auto-generated index" in out


def test_header_fields_exact():
    out = generate_index_md(
        [],
        "team:backend",
        generator="memorus 0.2.2",
        embedding_model="all-MiniLM-L6-v2",
        embedding_dim=384,
        now=FIXED_NOW,
    )
    assert "**Scope:** team:backend" in out
    assert "**Total bullets:** 0" in out
    assert "**Last updated:** 2026-04-23T10:15:00Z" in out
    assert "**Embedding model:** all-MiniLM-L6-v2 (dim=384)" in out
    assert "**Generated by:** memorus 0.2.2" in out


